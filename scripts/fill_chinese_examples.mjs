function normalizePos(pos = "") {
  return pos.toLowerCase();
}

function isVerb(pos) {
  return normalizePos(pos).includes("v");
}

function isAdjective(pos) {
  return normalizePos(pos).includes("adj");
}

function isNoun(pos) {
  return normalizePos(pos).includes("n");
}

function isMeasure(pos) {
  return normalizePos(pos).includes("m.");
}

