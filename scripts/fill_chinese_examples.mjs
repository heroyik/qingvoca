  if (actionObjectVerbs.has(word)) return `你可以先${word}一下。`;
  const linkingVerbs = new Set(["适合", "符合", "值得", "免费", "满", "缺少", "发生", "成为", "来自", "无法", "打折", "流行", "制冷", "受到", "举办", "举行", "超过", "出现", "来不及", "来得及", "应聘", "招聘", "通知", "满意", "赚钱"]);
