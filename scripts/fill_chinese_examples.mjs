function createExample(entry) {
  if (curatedExamples[entry.word]) return curatedExamples[entry.word];
  if (exactExamples[entry.word]) return exactExamples[entry.word];
  const pos = normalizePos(entry.partOfSpeech);
  if (isMeasure(pos)) return `我买了一${entry.word}新的。`;
  if (isAdjective(pos)) return createAdjectiveExample(entry.word);
  if (isVerb(pos)) return createVerbExample(entry.word);
  if (isNoun(pos)) return createNounExample(entry.word);
  return `这句话用${entry.word}更自然。`;
}

const source = JSON.parse(await readFile(inputPath, "utf8"));
if (!Array.isArray(source.words)) throw new Error(`${inputPath} must contain words[]`);

const errors = [];
for (const entry of source.words) {
  const example = createExample(entry);
  const examplePinyin = pinyin(example, { toneType: "symbol" });
  if (!example.includes(entry.word)) errors.push(`${entry.word}: example does not include word`);
  for (const banned of bannedFragments) {
    if (example.includes(banned)) errors.push(`${entry.word}: banned meta fragment ${banned}`);
  }
  entry.example = [example];
  entry.examplePinyin = [examplePinyin];
}

if (errors.length > 0) {
  for (const error of errors) console.error(`[examples] ${error}`);
  process.exit(1);
}

await writeFile(outputPath, `${JSON.stringify(source, null, 2)}\n`);
console.log(`[examples] updated ${source.words.length} entries in ${outputPath}`);
